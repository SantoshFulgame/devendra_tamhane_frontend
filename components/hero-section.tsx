"use client"

import { GraduationCap, Heart, BookOpen } from 'lucide-react'
import Image from 'next/image'
import { motion } from 'framer-motion'
import { useEffect, useState } from 'react'

export default function HeroSection() {
  const text = "35+ years of experience in education, mentoring, and social development"
  const [displayText, setDisplayText] = useState("")
  const [currentIndex, setCurrentIndex] = useState(0)

  useEffect(() => {
    if (currentIndex < text.length) {
      const timeout = setTimeout(() => {
        setDisplayText(prev => prev + text[currentIndex])
        setCurrentIndex(prev => prev + 1)
      }, 50) // Adjust typing speed here (lower number = faster)

      return () => clearTimeout(timeout)
    }
  }, [currentIndex])

  return (
    <div className="relative overflow-hidden bg-green-200">
      <div className="relative pt-16 pb-16 sm:pb-24">
        <div className="mx-auto max-w-7xl px-4 sm:px-6 lg:px-8">
          <div className="lg:grid lg:grid-cols-12 lg:gap-8">
            <div className="sm:text-center md:mx-auto md:max-w-2xl lg:col-span-6 lg:text-left">
              <div className="mt-24">
                <motion.h1 
                  className="text-4xl font-bold tracking-tight text-gray-900 sm:text-5xl md:text-6xl"
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ duration: 0.5 }}
                >
                  Devendra Anant Tamhane
                </motion.h1>
                <p className="mt-3 text-base text-gray-700 sm:mt-5 sm:text-xl lg:text-lg xl:text-xl min-h-[3.5rem]">
                  {displayText}
                  <motion.span 
                    className="inline-block w-0.5 h-5 bg-gray-700 ml-1"
                    animate={{ opacity: [1, 0] }}
                    transition={{ duration: 0.5, repeat: Infinity }}
                  />
                </p>
                <motion.div 
                  className="mt-8 flex flex-col gap-4 sm:flex-row sm:justify-center lg:justify-start"
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ duration: 0.5, delay: 0.2 }}
                >
                  <motion.div 
                    className="flex items-center gap-2 rounded-full bg-green-300 px-4 py-2 text-sm font-medium text-gray-800 shadow-sm hover:bg-green-400 transition-colors"
                    whileHover={{ scale: 1.05 }}
                    whileTap={{ scale: 0.95 }}
                  >
                    <GraduationCap className="h-5 w-5" />
                    MSc Physics, Mumbai University
                  </motion.div>
                  <motion.div 
                    className="flex items-center gap-2 rounded-full bg-green-300 px-4 py-2 text-sm font-medium text-gray-800 shadow-sm hover:bg-green-400 transition-colors"
                    whileHover={{ scale: 1.05 }}
                    whileTap={{ scale: 0.95 }}
                  >
                    <Heart className="h-5 w-5" />
                    Empowering Education
                  </motion.div>
                  <motion.div 
                    className="flex items-center gap-2 rounded-full bg-green-300 px-4 py-2 text-sm font-medium text-gray-800 shadow-sm hover:bg-green-400 transition-colors"
                    whileHover={{ scale: 1.05 }}
                    whileTap={{ scale: 0.95 }}
                  >
                    <BookOpen className="h-5 w-5" />
                    Creating Happiness
                  </motion.div>
                </motion.div>
              </div>
            </div>
            <div className="relative mt-12 sm:mx-auto sm:max-w-lg lg:col-span-6 lg:mx-0 lg:mt-0 lg:flex lg:max-w-none lg:items-center">
              <motion.div 
                className="relative mx-auto w-full rounded-full shadow-lg lg:max-w-md overflow-hidden"
                initial={{ opacity: 0, scale: 0.8 }}
                animate={{ opacity: 1, scale: 1 }}
                transition={{ duration: 0.5, delay: 0.3 }}
              >
                <Image
                  className="w-full rounded-full transition-transform duration-300 hover:scale-105"
                  src="/sir.webp"
                  alt="Profile"
                  width={200}
                  height={200}
                  priority
                />
              </motion.div>
            </div>
          </div>
        </div>
      </div>
    </div>
  )
}